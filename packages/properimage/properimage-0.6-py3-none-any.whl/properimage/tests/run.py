
import os
import sys

import pytest


# =============================================================================
# CONSTANTS
# =============================================================================

PATH = os.path.abspath(os.path.dirname(__file__))


# =============================================================================
# IMPORTS
# =============================================================================

def run(argv=None):
    argv = argv or []
    return pytest.main([PATH] + argv)


# =============================================================================
# MAIN
# =============================================================================

if __name__ == "__main__":
    sys.exit(run(sys.argv[1:]))
